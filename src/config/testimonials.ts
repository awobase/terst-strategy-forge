export function formatTestimonialAuthor(t: Testimonial): string {
  return `${t.firstName} ${t.lastInitial}.`;
}

export function formatTestimonialCaption(t: Testimonial): string {
  return `${t.role} — ${t.sector}`;
}

/** Normalise les variantes « Cayribe Partners » en CAYRIBE PARTNERS dans les témoignages */
export function formatTestimonialText(text: string): string {
  return text.replace(/Cayribe(\s+[Pp]artners)?/g, "CAYRIBE PARTNERS");
}
